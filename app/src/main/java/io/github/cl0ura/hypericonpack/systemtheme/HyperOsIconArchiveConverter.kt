    /**
     * Adds/replaces just one installed package in an already validated static
     * archive. This is intentionally not a second full conversion: all of an
     * icon pack's explicit appfilter mappings are copied verbatim and only the
     * new app's package default plus its launcher aliases are rendered.
     *
     * The resulting archive uses the new installed-package fingerprint and is
     * installed by [PackageThemeArchiveUpdateReceiver] after PACKAGE_ADDED.
     */
    internal fun updateInstalledPackage(
        context: Context,
        baseArchive: File,
        iconPackPackage: String,
        fallbackScaleMultiplier: Float,
        globalMonetIcons: Boolean,
        monetCustomColors: Boolean,
        monetBackgroundColor: Int,
        monetForegroundColor: Int,
        packageName: String,
    ): File = updateInstalledPackages(
        context = context,
        baseArchive = baseArchive,
        iconPackPackage = iconPackPackage,
        fallbackScaleMultiplier = fallbackScaleMultiplier,
        globalMonetIcons = globalMonetIcons,
        monetCustomColors = monetCustomColors,
        monetBackgroundColor = monetBackgroundColor,
        monetForegroundColor = monetForegroundColor,
        packageNames = listOf(packageName),
    )

    /**
     * Persists a coalesced package-install batch with one icon-pack parse and
     * one ZIP rewrite. App stores commonly update several packages together;
     * rewriting and installing the complete 30-150 MB archive once per package
     * wastes I/O and can keep Launcher under storage pressure for minutes.
     */
    internal fun updateInstalledPackages(
        context: Context,
        baseArchive: File,
        iconPackPackage: String,
        fallbackScaleMultiplier: Float,
        globalMonetIcons: Boolean,
        monetCustomColors: Boolean,
        monetBackgroundColor: Int,
        monetForegroundColor: Int,
        packageNames: Collection<String>,
    ): File {
        require(baseArchive.isFile) { "缺少当前主题归档" }
        val requestedPackages = packageNames.asSequence()
            .filter(String::isNotBlank)
            .distinct()
            .sorted()
            .toList()
        require(requestedPackages.isNotEmpty()) { "安装包名为空" }
        val baseInfo = readArchiveInfo(baseArchive)
            ?: throw IllegalStateException("当前主题归档元数据无法读取")
        require(baseInfo.isCurrentFormat) { "当前主题归档格式过旧，需要先重新转换一次" }
        require(baseInfo.iconPackPackage == iconPackPackage) { "当前主题来源与活动配置不一致" }
        require(baseInfo.globalMonetIcons == globalMonetIcons) { "当前主题 Monet 配置与活动配置不一致" }
        require(baseInfo.monetCustomColors == (globalMonetIcons && monetCustomColors)) {
            "当前主题 Monet 配色模式与活动配置不一致"
        }
        require(!globalMonetIcons || !monetCustomColors ||
            (baseInfo.monetBackgroundColor == monetBackgroundColor &&
                baseInfo.monetForegroundColor == monetForegroundColor)) {
            "当前主题 Monet 自定义配色与活动配置不一致"
        }
        require(baseInfo.fallbackScaleMultiplier?.let { kotlin.math.abs(it - fallbackScaleMultiplier) < 0.001f } == true) {
            "当前主题适配比例与活动配置不一致"
        }

        var parseFailure: Throwable? = null
        val pack = if (isOriginalIconSource(iconPackPackage)) {
            null
        } else {
            ParsedIconPack.load(context, iconPackPackage) { parseFailure = it }
                ?: throw IllegalStateException("无法读取当前图标包", parseFailure)
        }
        val palette = if (globalMonetIcons) {
            SystemMonetIconPalette.global(context, monetCustomColors, monetBackgroundColor, monetForegroundColor)
        } else {
            null
        }
        val launchEntriesByPackage = launchableActivities(context)
            .mapNotNull { info -> componentName(info)?.let { info.packageName to archiveEntryName(it) } }
            .groupBy(keySelector = { it.first }, valueTransform = { it.second })
        val replacementEntries = linkedMapOf<String, ByteArray>()

        requestedPackages.forEach { packageName ->
            val applicationInfo = context.packageManager.getApplicationInfo(
                packageName,
                PackageManager.MATCH_DISABLED_COMPONENTS,
            )
            val rawIcon = rawApplicationIcon(context, applicationInfo)
                ?: throw IllegalStateException("无法读取 $packageName 的原始图标")
            val mappedDrawable = mappedPackageDrawable(pack, packageName, prepareForMonet = globalMonetIcons)
            val preparedFallback = bestPackageDrawable(
                pack = pack,
                packageName = packageName,
                rawIcon = rawIcon,
                fallbackComponent = ComponentName(packageName, packageName),
                fallbackScaleMultiplier = fallbackScaleMultiplier,
                systemAdaptiveMask = null,
                prepareForMonet = globalMonetIcons,
            )
            val rawNativeMonochrome = if (globalMonetIcons && mappedDrawable == null) {
                nativeMonochromeSource(rawIcon)
            } else {
                null
            }
            val nativeMonochrome = rawNativeMonochrome?.let { native ->
                preparedFallback?.let { silhouette ->
                    native.copy(silhouette = silhouette, recoveryDrawable = silhouette)
                } ?: native
            }
            val styledDrawable = mappedDrawable ?: nativeMonochrome?.glyph ?: preparedFallback
                ?: throw IllegalStateException("无法构造 $packageName 的图标样式")
            val packagePng = renderIncrementalPng(styledDrawable, palette, nativeMonochrome)
                ?: throw IllegalStateException("无法渲染 $packageName 的图标")
            replacementEntries[packageArchiveEntryName(packageName)] = packagePng
            launchEntriesByPackage[packageName].orEmpty().distinct().forEach { entryName ->
                replacementEntries[entryName] = packagePng
            }
        }

        val scopeFingerprint = applicationScopeFingerprint(context)
        val destination = archiveFile(
            context = context,
            iconPackPackage = iconPackPackage,
            fallbackScaleMultiplier = fallbackScaleMultiplier,
            globalMonetIcons = globalMonetIcons,
            monetCustomColors = monetCustomColors,
            monetBackgroundColor = monetBackgroundColor,
            monetForegroundColor = monetForegroundColor,
            applicationScopeFingerprint = scopeFingerprint,
            createDirectory = true,
        )
        val temporary = File(destination.parentFile, "${destination.name}.package-update")
        try {
            ZipFile(baseArchive).use { input ->
                ZipOutputStream(BufferedOutputStream(FileOutputStream(temporary))).use { output ->
                    output.setLevel(Deflater.NO_COMPRESSION)
                    writeArchiveMetadata(
                        zip = output,
                        iconPackPackage = iconPackPackage,
                        fallbackScaleMultiplier = fallbackScaleMultiplier,
                        globalMonetIcons = globalMonetIcons,
                        monetCustomColors = monetCustomColors,
                        monetBackgroundColor = monetBackgroundColor,
                        monetForegroundColor = monetForegroundColor,
                        applicationScopeFingerprint = scopeFingerprint,
                    )
                    writeTransformConfig(output, useDynamicIcon = false)
                    input.entries().asSequence().forEach { entry ->
                        if (
                            entry.name == METADATA_ENTRY ||
                            entry.name == TRANSFORM_CONFIG_ENTRY ||
                            entry.name in replacementEntries
                        ) return@forEach
                        output.putNextEntry(ZipEntry(entry.name))
                        try {
                            if (!entry.isDirectory) input.getInputStream(entry).use { it.copyTo(output) }
                        } finally {
                            output.closeEntry()
                        }
                    }
                    replacementEntries.forEach { (entryName, png) ->
                        output.putNextEntry(ZipEntry(entryName))
                        try {
                            output.write(png)
                        } finally {
                            output.closeEntry()
                        }
                    }
                }
            }
            ZipFile(temporary).use { zip ->
                replacementEntries.keys.forEach { entryName ->
                    require(zip.getEntry(entryName)?.size ?: 0L > 0L) {
                        "增量主题归档校验失败：缺少 $entryName"
                    }
                }
            }
            if (destination.exists() && !destination.delete()) {
                throw IllegalStateException("无法替换旧的增量主题归档")
            }
            if (!temporary.renameTo(destination)) {
                throw IllegalStateException("无法提交增量主题归档")
            }
            val destinationDynamic = dynamicArchiveFor(destination)
            // Dynamic calendar trees are embedded in the primary icons ZIP.
            // Never carry a legacy sidecar forward: the installer deliberately
            // keeps /data/system/theme/dynamicicons out of the active lookup
            // path so Xiaomi's stock Adaptive tree cannot override pack shape.
            destinationDynamic.delete()
            return destination
        } catch (throwable: Throwable) {
            temporary.delete()
            throw throwable
        }
    }

    /**
     * Renders one installed package for the exported read-only icon provider.
     * This is the live equivalent of an incremental archive entry and uses the
     * exact same pack fallback, shape and Monet rules as offline conversion.
     */
    internal fun renderInstalledPackagePng(
        context: Context,
        iconPackPackage: String,
        fallbackScaleMultiplier: Float,
        globalMonetIcons: Boolean,
        monetCustomColors: Boolean,
        monetBackgroundColor: Int,
        monetForegroundColor: Int,
        packageName: String,
    ): ByteArray? = runCatching {
        val applicationInfo = context.packageManager.getApplicationInfo(
            packageName,
            PackageManager.MATCH_DISABLED_COMPONENTS,
        )
        val rawIcon = rawApplicationIcon(context, applicationInfo)
            ?: throw IllegalStateException("无法读取 $packageName 的原始图标")
        val pack = if (isOriginalIconSource(iconPackPackage)) {
            null
        } else {
            ParsedIconPack.load(context, iconPackPackage)
                ?: throw IllegalStateException("无法读取当前图标包")
        }
        val mapped = mappedPackageDrawable(pack, packageName, prepareForMonet = globalMonetIcons)
        val prepared = bestPackageDrawable(
            pack = pack,
            packageName = packageName,
            rawIcon = rawIcon,
            fallbackComponent = ComponentName(packageName, packageName),
            fallbackScaleMultiplier = fallbackScaleMultiplier,
            systemAdaptiveMask = null,
            prepareForMonet = globalMonetIcons,
        ) ?: throw IllegalStateException("无法构造 $packageName 的图标样式")
        val rawNative = if (globalMonetIcons && mapped == null) nativeMonochromeSource(rawIcon) else null
        val native = rawNative?.copy(silhouette = prepared, recoveryDrawable = prepared)
        val drawable = mapped ?: native?.glyph ?: prepared
        val palette = if (globalMonetIcons) {
            SystemMonetIconPalette.global(context, monetCustomColors, monetBackgroundColor, monetForegroundColor)
        } else {
            null
        }
        renderIncrementalPng(drawable, palette, native)
    }.getOrNull()

    /** All valid cached archives, used by the settings UI for fast switching. */
    fun cachedArchiveInfos(context: Context): List<ExistingArchiveInfo> {
        val directory = archiveDirectory(context, createDirectory = false) ?: return legacyArchive(context)
            .takeIf(File::isFile)
            ?.let(::readArchiveInfo)
            ?.let(::listOf)
            .orEmpty()
        val cached = directory.listFiles { file ->
            file.isFile && file.name.startsWith(ARCHIVE_FILE_PREFIX) && file.extension == "zip"
        }.orEmpty().mapNotNull(::readArchiveInfo)
        val legacy = legacyArchive(context).takeIf(File::isFile)?.let(::readArchiveInfo)
        return (cached + listOfNotNull(legacy))
            .filter(ExistingArchiveInfo::isCurrentFormat)
            .distinctBy { it.archive.absolutePath }
            .sortedWith(compareBy(String.CASE_INSENSITIVE_ORDER) { it.iconPackPackage ?: it.archive.name })
    }

    /**
     * Finds the newest valid archive for a source, regardless of the installed
     * application fingerprint embedded in its filename.  The fingerprint is
     * intentionally part of the conversion cache key, so installing or
     * removing an app can make the exact-key lookup miss even while the
     * previously applied archive is still the active system theme.
     */
    fun latestCachedArchiveForSource(
        context: Context,
        iconPackPackage: String?,
    ): ExistingArchiveInfo? {
        if (iconPackPackage.isNullOrBlank()) return null
        return cachedArchiveInfos(context)
            .asSequence()
            .filter { it.iconPackPackage == iconPackPackage }
            .maxByOrNull { it.archive.lastModified() }
    }

    /**
     * Removes only a converter-owned cache file. The active HyperOS archive
     * under /data/system/theme is intentionally untouched: deleting a cache
     * must never silently change the icon theme currently visible to the user.
     */
    fun deleteCachedArchive(context: Context, archive: File): Boolean {
        val directory = archiveDirectory(context, createDirectory = false) ?: return false
        val expectedParent = runCatching { directory.canonicalFile }.getOrNull() ?: return false
        val candidate = runCatching { archive.canonicalFile }.getOrNull() ?: return false
        if (candidate.parentFile != expectedParent) return false
        if (!candidate.name.startsWith(ARCHIVE_FILE_PREFIX) || !candidate.name.endsWith(".zip")) return false
        val deleted = !candidate.exists() || candidate.delete()
        if (deleted) dynamicArchiveFor(candidate).delete()
        return deleted
    }

    fun dynamicArchiveFor(iconArchive: File): File = File(
        iconArchive.parentFile,
        "${iconArchive.nameWithoutExtension}$DYNAMIC_ARCHIVE_SUFFIX",
    )

    /**
     * Copies only classic MAML entries into the main icons ZIP.  Keeping these
     * entries in the primary archive is intentional: when UseDynamicIcon is
     * disabled, ThemeResourcesSystem never consults the stock dynamicicons
     * archive and IconCustomizer resolves our alpha-preserving classic tree.
     */
    private fun embedClassicDynamicArchive(iconArchive: File, dynamicArchive: File) {
        val temporary = File(iconArchive.parentFile, "${iconArchive.name}.embed-dynamic.new")
        ZipFile(iconArchive).use { icons ->
            ZipFile(dynamicArchive).use { dynamic ->
                ZipOutputStream(BufferedOutputStream(FileOutputStream(temporary))).use { output ->
                    output.setLevel(Deflater.NO_COMPRESSION)
                    writeTransformConfig(output, useDynamicIcon = false)
                    val written = HashSet<String>()
                    icons.entries().asSequence().forEach { entry ->
                        if (entry.name == TRANSFORM_CONFIG_ENTRY) return@forEach
                        if (!written.add(entry.name)) return@forEach
                        output.putNextEntry(ZipEntry(entry.name))
                        try {
                            if (!entry.isDirectory) {
                                icons.getInputStream(entry).use { it.copyTo(output) }
                            }
                        } finally {
                            output.closeEntry()
                        }
                    }
                    dynamic.entries().asSequence()
                        .filter { it.name.startsWith("animating_icons/") }
                        .forEach { entry ->
                            if (!written.add(entry.name)) return@forEach
                            output.putNextEntry(ZipEntry(entry.name))
                            try {
                                if (!entry.isDirectory) {
                                    dynamic.getInputStream(entry).use { it.copyTo(output) }
                                }
                            } finally {
                                output.closeEntry()
                            }
                        }
                }
            }
        }
        if (!iconArchive.delete()) {
            temporary.delete()
            throw IllegalStateException("无法合并动态图标资源到主题归档")
        }
        if (!temporary.renameTo(iconArchive)) {
            temporary.delete()
            throw IllegalStateException("无法提交合并后的主题归档")
        }
    }

    /** Keeps the archive's dynamic lookup flag aligned with the installer. */
    fun ensureUseDynamicIconTransformConfig(
        iconArchive: File,
        enableDynamicIcons: Boolean,
    ): File {
        ZipFile(iconArchive).use { zip ->
            val existing = zip.getEntry(TRANSFORM_CONFIG_ENTRY)
            if (existing != null) {
                val content = zip.getInputStream(existing).use { it.readBytes().toString(Charsets.UTF_8) }
                val expected = enableDynamicIcons.toString()
                if (
                    content.contains("name=\"UseDynamicIcon\" value=\"$expected\"") &&
                    content.contains("name=\"SupportLayerIcon\" value=\"false\"")
                ) {
                    return iconArchive
                }
            }
        }
        val temporary = File(iconArchive.parentFile, "${iconArchive.name}.dynamic-config.new")
        ZipFile(iconArchive).use { input ->
            ZipOutputStream(BufferedOutputStream(FileOutputStream(temporary))).use { output ->
                output.setLevel(Deflater.NO_COMPRESSION)
                writeTransformConfig(output, useDynamicIcon = enableDynamicIcons)
                input.entries().asSequence().forEach { entry ->
                    if (entry.name == TRANSFORM_CONFIG_ENTRY) return@forEach
                    output.putNextEntry(ZipEntry(entry.name))
                    try {
                        if (!entry.isDirectory) {
                            input.getInputStream(entry).use { stream -> stream.copyTo(output) }
                        }
                    } finally {
                        output.closeEntry()
                    }
                }
            }
        }
        if (!iconArchive.delete()) {
            temporary.delete()
            throw IllegalStateException("无法更新主题归档的动态图标配置")
        }
        if (!temporary.renameTo(iconArchive)) {
            temporary.delete()
            throw IllegalStateException("无法提交主题归档的动态图标配置")
        }
        return iconArchive
    }

    private fun readArchiveInfo(archive: File): ExistingArchiveInfo? {
        return runCatching {
            ZipFile(archive).use { zip ->
                val entry = zip.getEntry(METADATA_ENTRY) ?: return@use ExistingArchiveInfo(
                    archive = archive,
                    iconPackPackage = null,
                    fallbackScaleMultiplier = null,
                    globalMonetIcons = false,
                    monetCustomColors = false,
                    monetBackgroundColor = IconPackConfig.DEFAULT_MONET_BACKGROUND_COLOR,
                    monetForegroundColor = IconPackConfig.DEFAULT_MONET_FOREGROUND_COLOR,
                    applicationScopeFingerprint = ALL_APPLICATIONS_SCOPE,
                    formatVersion = null,
                )
                val values = linkedMapOf<String, String>()
                zip.getInputStream(entry).bufferedReader().useLines { lines ->
                    lines.forEach { line ->
                        val divider = line.indexOf('=')
                        if (divider > 0) {
                            values[line.substring(0, divider)] = line.substring(divider + 1)
                        }
                    }
                }
                ExistingArchiveInfo(
                    archive = archive,
                    iconPackPackage = values["icon_pack_package"]?.takeIf(String::isNotBlank),
                    fallbackScaleMultiplier = values["fallback_scale"]?.toFloatOrNull(),
                    // Rendering revisions invalidate conversion caches, but
                    // older saved Monet archives remain Monet archives when
                    // reapplied or incrementally updated after an app install.
                    globalMonetIcons = values["icon_rendering"]
                        ?.startsWith(GLOBAL_MONET_RENDERING_PREFIX) == true,
                    monetCustomColors = values["monet_palette"] == "custom",
                    monetBackgroundColor = values["monet_background"]
                        ?.toLongOrNull(16)
                        ?.toInt()
                        ?: IconPackConfig.DEFAULT_MONET_BACKGROUND_COLOR,
                    monetForegroundColor = values["monet_foreground"]
                        ?.toLongOrNull(16)
                        ?.toInt()
                        ?: IconPackConfig.DEFAULT_MONET_FOREGROUND_COLOR,
                    applicationScopeFingerprint = values["application_scope"] ?: ALL_APPLICATIONS_SCOPE,
                    formatVersion = values["format"]?.toIntOrNull(),
                )
            }
        }.getOrElse {
            // The file exists and may already be installed. Do not treat a
            // malformed/legacy metadata entry as a failed conversion.
            ExistingArchiveInfo(
                archive = archive,
                iconPackPackage = null,
                fallbackScaleMultiplier = null,
                globalMonetIcons = false,
                monetCustomColors = false,
                monetBackgroundColor = IconPackConfig.DEFAULT_MONET_BACKGROUND_COLOR,
                monetForegroundColor = IconPackConfig.DEFAULT_MONET_FOREGROUND_COLOR,
                applicationScopeFingerprint = ALL_APPLICATIONS_SCOPE,
                formatVersion = null,
            )
        }
    }

    private fun archiveFile(
        context: Context,
        iconPackPackage: String,
        fallbackScaleMultiplier: Float,
        globalMonetIcons: Boolean,
        monetCustomColors: Boolean,
        monetBackgroundColor: Int,
        monetForegroundColor: Int,
        applicationScopeFingerprint: String,
        createDirectory: Boolean,
    ): File {
        val root = context.getExternalFilesDir(ARCHIVE_DIRECTORY)
            ?: throw IllegalStateException("外部应用文件目录不可用，无法创建待应用的主题归档")
        val directory = File(root, ARCHIVE_CACHE_DIRECTORY)
        if (createDirectory) {
            ensureArchiveDirectoryWritable(directory)
        } else if (!directory.exists() && !directory.mkdirs()) {
            throw IllegalStateException("无法创建主题归档目录：${directory.absolutePath}")
        }
        // Preserve the pre-Monet identity for the ordinary converter path so
        // a user's durable v4 cache remains reusable. This global renderer
        // gets a separate identity and can never overwrite it.
        val identity = buildString {
            append("format=").append(CURRENT_FORMAT_VERSION).append('|')
            append(iconPackPackage)
            append('|').append("%.3f".format(Locale.ROOT, fallbackScaleMultiplier))
            if (globalMonetIcons) append("|render=").append(GLOBAL_MONET_RENDERING)
            if (globalMonetIcons && monetCustomColors) {
                append("|palette=custom")
                append("|background=").append(monetBackgroundColor.toUInt().toString(16))
                append("|foreground=").append(monetForegroundColor.toUInt().toString(16))
            }
            if (applicationScopeFingerprint != ALL_APPLICATIONS_SCOPE) append("|apps=").append(applicationScopeFingerprint)
        }
        val digest = MessageDigest.getInstance("SHA-256")
            .digest(identity.toByteArray(Charsets.UTF_8))
            .joinToString("") { byte -> "%02x".format(Locale.ROOT, byte) }
        return File(directory, "$ARCHIVE_FILE_PREFIX${digest.take(20)}.zip")
    }

    fun applicationScopeFingerprint(context: Context): String {
        return packageSetFingerprint(installedApplications(context).map(ApplicationInfo::packageName))
    }

    private fun packageSetFingerprint(packages: Collection<String>): String {
        val normalized = packages.filter(String::isNotBlank).sorted()
        return MessageDigest.getInstance("SHA-256")
            .digest(normalized.joinToString("\n").toByteArray(Charsets.UTF_8))
            .joinToString("") { byte -> "%02x".format(Locale.ROOT, byte) }
            .take(SCOPE_FINGERPRINT_LENGTH)
    }


    /**
     * External archive storage lives under Android/data.  After uninstall/
     * reinstall the directory tree can remain owned by the previous app uid
     * (and an older multi-category SELinux context).  Future writes then fail
     * with EACCES even though getExternalFilesDir() returns a path.  Reclaim
     * ownership once when we cannot write.
     */
    private fun ensureArchiveDirectoryWritable(directory: File) {
        if (directory.exists() || directory.mkdirs()) {
            val probe = File(directory, ".write-probe")
            val writable = runCatching {
                probe.outputStream().use { it.write(0) }
                probe.delete()
                true
            }.getOrDefault(false)
            if (writable) return
        }

        val path = directory.absolutePath.replace("'", "'\''")
        val result = RootAccess.runFixed(
            command = """
                set -e
                path='$path'
                app_uid="${'$'}(stat -c %u /data/user/0/io.github.cl0ura.hypericonpack 2>/dev/null || true)"
                [ -n "${'$'}app_uid" ]
                mkdir -p "${'$'}path"
                # Keep the media group for FUSE visibility, but the owner must
                # match the currently installed app uid.
                chown -R "${'$'}app_uid:ext_data_rw" "${'$'}(dirname "${'$'}(dirname "${'$'}path")")" 2>/dev/null || true
                chown -R "${'$'}app_uid:ext_data_rw" "${'$'}path"
                find "${'$'}path" -type d -exec chmod 2770 {} +
                find "${'$'}path" -type f -exec chmod 660 {} +
                chcon -R u:object_r:media_rw_data_file:s0 "${'$'}path" 2>/dev/null || true
                touch "${'$'}path/.write-probe"
                chown "${'$'}app_uid:ext_data_rw" "${'$'}path/.write-probe"
                chmod 660 "${'$'}path/.write-probe"
                rm -f "${'$'}path/.write-probe"
                echo 'HYPER_ICONPACK_ARCHIVE_DIR_OK'
            """.trimIndent(),
            timeoutSeconds = 20L,
        )
        if (!result.success || !result.output.contains("HYPER_ICONPACK_ARCHIVE_DIR_OK")) {
            throw IllegalStateException(
                "主题归档目录不可写：${directory.absolutePath}. ${result.output}".trim(),
            )
        }
        if (!directory.exists() && !directory.mkdirs()) {
            throw IllegalStateException("无法创建主题归档目录：${directory.absolutePath}")
        }
        val probe = File(directory, ".write-probe")
        runCatching {
            probe.outputStream().use { it.write(0) }
            probe.delete()
        }.getOrElse {
            throw IllegalStateException(
                "主题归档目录仍不可写：${directory.absolutePath}",
                it,
            )
        }
    }

    private fun archiveDirectory(context: Context, createDirectory: Boolean): File? {
        val root = context.getExternalFilesDir(ARCHIVE_DIRECTORY) ?: return null
        val directory = File(root, ARCHIVE_CACHE_DIRECTORY)
        if (createDirectory) {
            ensureArchiveDirectoryWritable(directory)
        }
        return directory.takeIf { it.exists() || createDirectory }
    }

