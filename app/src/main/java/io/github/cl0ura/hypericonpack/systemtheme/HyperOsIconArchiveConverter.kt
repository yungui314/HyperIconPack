    private fun renderDrawableAsPng(
        drawable: Drawable,
        output: OutputStream,
        size: Int = RENDER_SIZE_PX,
    ) {
        val bitmap = Bitmap.createBitmap(size, size, Bitmap.Config.ARGB_8888)
        val canvas = Canvas(bitmap)
        val oldBounds = Rect(drawable.bounds)
        try {
            drawable.setBounds(0, 0, size, size)
            drawable.draw(canvas)
            if (!bitmap.compress(Bitmap.CompressFormat.PNG, 100, output)) {
                throw IllegalStateException("PNG 编码失败")
            }
        } finally {
            drawable.bounds = oldBounds
            bitmap.recycle()
        }
    }

    private fun renderIncrementalPng(
        drawable: Drawable,
        palette: GlobalMonetPalette?,
        nativeMonochrome: NativeMonochromeSource? = null,
    ): ByteArray? = runCatching {
        ByteArrayOutputStream().use { output ->
            if (palette == null) {
                renderDrawableAsPng(drawable, output)
            } else {
                val rendered = runCatching {
                    if (nativeMonochrome != null) {
                        GlobalMonetIconRenderer.renderNativeMonochrome(
                            glyph = nativeMonochrome.glyph,
                            silhouette = nativeMonochrome.silhouette,
                            palette = palette,
                            size = RENDER_SIZE_PX,
                            output = output,
                        )
                    } else {
                        GlobalMonetIconRenderer.render(
                            drawable = drawable,
                            palette = palette,
                            size = RENDER_SIZE_PX,
                            output = output,
                        )
                    }
                }.isSuccess || run {
                    output.reset()
                    runCatching {
                        GlobalMonetIconRenderer.renderSimplified(
                            drawable = nativeMonochrome?.recoveryDrawable ?: drawable,
                            palette = palette,
                            size = RENDER_SIZE_PX,
                            output = output,
                        )
                    }.isSuccess
                }
                if (!rendered) {
                    output.reset()
                    val guaranteed = runCatching {
                        GlobalMonetIconRenderer.renderGuaranteed(
                            drawable = nativeMonochrome?.recoveryDrawable ?: drawable,
                            palette = palette,
                            size = RENDER_SIZE_PX,
                            output = output,
                        )
                    }.isSuccess
                    if (!guaranteed) {
                        return@use null
                    }
                }
            }
            output.toByteArray().takeIf { it.isNotEmpty() }
        }
    }.getOrNull()

    /**
     * HyperOS only consults /data/system/theme/dynamicicons when the icons
     * archive declares UseDynamicIcon=true. Without this entry, calendar MAML
     * resources are installed but never loaded by IconCustomizer.
     */
    private fun writeTransformConfig(zip: ZipOutputStream, useDynamicIcon: Boolean) {
        // LayerAdaptiveIconDrawable treats a transparent adaptive background as
        // an opaque black canvas on the tested HyperOS 3 launcher. Keep layer
        // lookup disabled so every surface consumes the same alpha-preserving
        // main PNG; this also prevents partial fallback to stock adaptive icons
        // when a theme archive has no complete layer set.
        // Classic MAML entries embedded in the main icons archive must keep
        // UseDynamicIcon=false; otherwise the stock dynamicicons layer is
        // consulted first and AdaptiveIconDrawable re-masks pack shapes.
        val config = """
            <?xml version="1.0" encoding="UTF-8"?>
            <IconTransform>
                <Config name="SupportLayerIcon" value="false" />
                <Config name="UseDynamicIcon" value="$useDynamicIcon" />
                <Config name="ConfigIconMask" value="M0 0H100V100H0Z" />
            </IconTransform>
        """.trimIndent()
        zip.putNextEntry(ZipEntry(TRANSFORM_CONFIG_ENTRY))
        try {
            zip.write(config.toByteArray(Charsets.UTF_8))
        } finally {
            zip.closeEntry()
        }
    }

    private fun writeArchiveMetadata(
        zip: ZipOutputStream,
        iconPackPackage: String,
        fallbackScaleMultiplier: Float,
        globalMonetIcons: Boolean,
        monetCustomColors: Boolean,
        monetBackgroundColor: Int,
        monetForegroundColor: Int,
        applicationScopeFingerprint: String,
    ) {
        val metadata = buildString {
            append("format=").append(CURRENT_FORMAT_VERSION).append('\n')
            append("icon_pack_package=").append(iconPackPackage).append('\n')
            append("fallback_scale=").append(fallbackScaleMultiplier).append('\n')
            append("icon_rendering=")
                .append(if (globalMonetIcons) GLOBAL_MONET_RENDERING else DEFAULT_RENDERING)
                .append('\n')
            append("monet_palette=")
                .append(if (globalMonetIcons && monetCustomColors) "custom" else "system")
                .append('\n')
            append("monet_background=").append(monetBackgroundColor.toUInt().toString(16)).append('\n')
            append("monet_foreground=").append(monetForegroundColor.toUInt().toString(16)).append('\n')
            append("application_scope=").append(applicationScopeFingerprint).append('\n')
        }
        zip.putNextEntry(ZipEntry(METADATA_ENTRY))
        try {
            zip.write(metadata.toByteArray(Charsets.UTF_8))
        } finally {
            zip.closeEntry()
        }
    }

    private fun validateArchive(file: File, expectedIconEntries: Int) {
        ZipFile(file).use { zip ->
            val iconEntries = zip.entries().asSequence()
                .count { entry -> !entry.isDirectory && entry.name.startsWith("$TARGET_DENSITY_DIRECTORY/") && entry.name.endsWith(".png") }
            require(iconEntries == expectedIconEntries) {
                "主题归档校验失败：期望 $expectedIconEntries 个图标，实际 $iconEntries 个"
            }
            require(zip.getEntry(TRANSFORM_CONFIG_ENTRY) != null) {
                "主题归档校验失败：缺少 $TRANSFORM_CONFIG_ENTRY"
            }
        }
    }

    private fun reportProgress(
        callback: ((ConversionProgress) -> Unit)?,
        phase: ConversionPhase,
        phaseCompleted: Int,
        phaseTotal: Int,
        totalCompleted: Int,
        total: Int,
        explicitCompleted: Int,
        explicitTotal: Int,
        fallbackCompleted: Int,
        fallbackTotal: Int,
        force: Boolean = false,
    ) {
        if (callback == null || (!force && phaseCompleted % PROGRESS_REPORT_INTERVAL != 0)) return
        callback(
            ConversionProgress(
                phase = phase,
                completed = totalCompleted,
                total = total,
                explicitCompleted = explicitCompleted,
                explicitTotal = explicitTotal,
                fallbackCompleted = fallbackCompleted,
                fallbackTotal = fallbackTotal,
            ),
        )
    }
