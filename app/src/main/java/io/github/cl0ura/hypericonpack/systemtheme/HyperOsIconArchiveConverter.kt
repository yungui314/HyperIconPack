            .toList()

    private fun componentName(activityInfo: ActivityInfo): ComponentName? {
        val packageName = activityInfo.packageName.takeIf(String::isNotBlank) ?: return null
        val rawClassName = activityInfo.name.takeIf(String::isNotBlank) ?: return null
        val className = if (rawClassName.startsWith('.')) packageName + rawClassName else rawClassName
        return ComponentName(packageName, className)
    }

    /**
     * This intentionally uses PackageManager#getDrawable, the same raw app
     * resource lookup IconCustomizer itself uses before applying a theme.  It
     * avoids recursively baking an already themed icon into the fallback.
     */
    private fun rawApplicationIcon(context: Context, applicationInfo: ApplicationInfo): Drawable? {
        val packageManager = context.packageManager
        val resourceId = applicationInfo.icon
        val declared = if (resourceId != 0) {
            runCatching {
                packageManager.getDrawable(applicationInfo.packageName, resourceId, applicationInfo)
            }.getOrNull()
        } else {
            null
        }
        // Split/resource-overlay packages occasionally declare an icon id
        // that PackageManager cannot resolve from the base APK. Fall through
        // to getApplicationIcon instead of dropping the package from live or
        // incremental conversion entirely.
        return declared ?: runCatching { packageManager.getApplicationIcon(applicationInfo) }.getOrNull()
    }

    /**
     * Resolves an Activity-specific icon without passing it through themed
     * icon lookup. Most launch activities inherit the application icon, but
     * a few apps deliberately define a separate adaptive icon for a shortcut
     * or entry point.
     */
    private fun rawActivityIcon(context: Context, activityInfo: ActivityInfo): Drawable? {
        val resourceId = activityInfo.icon
        return if (resourceId != 0) {
            runCatching {
                context.packageManager.getDrawable(
                    activityInfo.packageName,
                    resourceId,
                    activityInfo.applicationInfo,
                )
            }.getOrNull()
        } else {
            rawApplicationIcon(context, activityInfo.applicationInfo)
        }
    }

    /** Finds one raw AdaptiveIconDrawable whose icon mask reflects this OEM. */
    private fun resolveSystemAdaptiveMask(
        context: Context,
        applications: List<ApplicationInfo>,
    ): AdaptiveIconDrawable? {
        val packageManager = context.packageManager
        val preferredPackages = listOf("com.miui.home", "com.android.settings", "com.android.systemui")
        val preferred = preferredPackages.asSequence()
            .mapNotNull { packageName ->
                applications.firstOrNull { it.packageName == packageName }
                    ?: runCatching { packageManager.getApplicationInfo(packageName, 0) }.getOrNull()
            }
        return (preferred + applications.asSequence())
            .mapNotNull { info -> rawApplicationIcon(context, info) as? AdaptiveIconDrawable }
            .firstOrNull()
            ?.let { drawable ->
                drawable.constantState?.newDrawable()?.mutate() as? AdaptiveIconDrawable ?: drawable
            }
    }

    /**
     * Android 13 introduced [AdaptiveIconDrawable.monochrome] for launcher
     * themed icons.  Unlike a colour/luminance heuristic, this layer is an
     * application-provided semantic glyph and is therefore the most faithful
     * possible source for Monet.  Its original AdaptiveIconDrawable is kept
     * as the shape source, so the converter does not force a circular frame.
     * The API guard keeps the converter compatible with older Android
     * versions.
     */
    private fun nativeMonochromeSource(drawable: Drawable?): NativeMonochromeSource? {
        if (Build.VERSION.SDK_INT < Build.VERSION_CODES.TIRAMISU) return null
        val adaptiveIcon = drawable as? AdaptiveIconDrawable ?: return null
        val monochrome = adaptiveIcon.monochrome ?: return null
        val glyph = monochrome.constantState?.newDrawable()?.mutate() ?: monochrome.mutate()
        val rawSilhouette = adaptiveIcon.constantState?.newDrawable()?.mutate() ?: adaptiveIcon.mutate()
        // `AdaptiveIconDrawable#draw` is permitted to paint its background
        // across the whole temporary canvas.  In the normal launcher pipeline
        // an adaptive mask is applied afterwards, but a static PNG has no
        // such later stage.  Always bake that mask now; otherwise just turning
        // on Global Monet can make a circular icon-pack fallback become a
        // square coloured tile.
        val silhouette = SystemIconShapeDrawable(
            source = rawSilhouette,
            systemMask = null,
        )
        return NativeMonochromeSource(
            glyph = glyph,
            silhouette = silhouette,
            recoveryDrawable = rawSilhouette,
        )
    }

    /**
     * Chooses the exact appfilter mapping when one is usable; otherwise bakes
     * the public iconback/mask/upon fallback once into the archive.  This is
     * intentionally converter-only: no live launcher or SystemUI Drawable is
     * replaced at runtime.
     */
    private fun bestPackageDrawable(
        pack: ParsedIconPack?,
        packageName: String,
        rawIcon: Drawable?,
        fallbackComponent: ComponentName,
        fallbackScaleMultiplier: Float,
        systemAdaptiveMask: AdaptiveIconDrawable?,
        prepareForMonet: Boolean = false,
    ): Drawable? {
        fun systemShapedRawIcon(): Drawable? = rawIcon?.let { source ->
            SystemIconShapeDrawable(source = source, systemMask = systemAdaptiveMask)
        }
        if (pack == null) return systemShapedRawIcon()
        val explicit = mappedPackageDrawable(pack, packageName, prepareForMonet)
        if (explicit != null) return explicit
        if (rawIcon == null) return null
        // A lack of iconback/iconmask must never remove an installed system
        // application from the archive. Keep its raw icon as a generic
        // fallback; the global Monet renderer will still normalise it when
        // enabled, while normal mode at least remains functionally complete.
        if (!pack.supportsFallbackStyle) return systemShapedRawIcon()
        return pack.createFallback(
            original = rawIcon,
            component = fallbackComponent,
            scaleMultiplier = fallbackScaleMultiplier,
        ) ?: systemShapedRawIcon()
    }

    /** Returns a usable appfilter asset for an installed package, if any. */
    private fun mappedPackageDrawable(
        pack: ParsedIconPack?,
        packageName: String,
        prepareForMonet: Boolean = false,
    ): Drawable? {
        val resolvedPack = pack ?: return null
        val component = ComponentName(packageName, packageName)
        return resolvedPack.mappedDrawableNamesForPackage(packageName)
            .asSequence()
            .mapNotNull { drawableName ->
                if (prepareForMonet) {
                    resolvedPack.loadMappedDrawableForMonet(drawableName, component)
                } else {
                    resolvedPack.loadMappedDrawable(drawableName)
                }
            }
            .firstOrNull()
    }

    /** Converts standard appfilter calendar prefixes into HyperOS MAML dynamic icons. */
    private fun generateDynamicCalendarArchive(
        pack: ParsedIconPack?,
        installedPackageNames: Set<String>,
        launchableActivities: List<ActivityInfo>,
        iconArchive: File,
        globalMonetPalette: GlobalMonetPalette?,
        fallbackScaleMultiplier: Float,
    ): File? {
        val resolvedPack = pack ?: return null
        // HyperOS resolves dynamic icons through IconCustomizer.getIconNames():
        // activity class first (e.g. com.android.calendar.homepage.AllInOneActivity),
        // then package fallback. Emitting only the package root makes the desktop
        // miss the pack's dynamic tree and fall back to the stock square MAML icon.
        val installedMappings = resolvedPack.calendarMappings()
            .filter { it.component.packageName in installedPackageNames }
        if (installedMappings.isEmpty()) {
            dynamicArchiveFor(iconArchive).delete()
            return null
        }
        val launchableByPackage = launchableActivities
            .mapNotNull { info -> componentName(info) }
            .groupBy({ it.packageName }, { it })
        val destination = dynamicArchiveFor(iconArchive)
        val temporary = File(destination.parentFile, "${destination.name}.new")
        var convertedTrees = 0
        try {
            ZipOutputStream(BufferedOutputStream(FileOutputStream(temporary))).use { zip ->
                zip.setLevel(Deflater.NO_COMPRESSION)
                val renderedByPrefix = HashMap<String, List<ByteArray>>()
                val writtenRoots = HashSet<String>()
                installedMappings
                    .groupBy { it.component.packageName to it.drawablePrefix }
                    .forEach { (packageAndPrefix, mappings) ->
                        val (packageName, drawablePrefix) = packageAndPrefix
                        val renderedDays = renderedByPrefix.getOrPut(drawablePrefix) {
                            val frames = (1..31).map { day ->
                                val drawable = resolvedPack.loadCalendarDrawable(
                                    drawablePrefix = drawablePrefix,
                                    dayOfMonth = day,
                                    scaleMultiplier = fallbackScaleMultiplier,
                                ) ?: return@map null
                                // Reuse the exact static Monet/original pipeline so
                                // dynamic calendar frames keep the same shape and palette.
                                renderIncrementalPng(
                                    drawable = drawable,
                                    palette = globalMonetPalette,
                                    nativeMonochrome = null,
                                )
                            }
                            if (frames.any { it == null }) {
                                emptyList()
                            } else {
                                frames.filterNotNull()
                            }
                        }
                        if (renderedDays.size != 31) return@forEach
                        val relativeNames = LinkedHashSet<String>()
                        relativeNames += packageName
                        mappings.forEach { mapping ->
                            relativeNames += dynamicIconLookupName(
                                packageName = mapping.component.packageName,
                                className = mapping.component.className,
                            )
                        }
                        launchableByPackage[packageName].orEmpty().forEach { component ->
                            relativeNames += dynamicIconLookupName(
                                packageName = component.packageName,
                                className = component.className,
                            )
                        }
                        val currentDay = Calendar.getInstance()
                            .get(Calendar.DAY_OF_MONTH)
                            .coerceIn(1, 31)
                        val quiet = renderedDays[currentDay - 1]
                        val manifest = dynamicCalendarManifest()
                        relativeNames.forEach { relativeName ->
                            if (!writtenRoots.add(relativeName)) return@forEach
                            // Keep the finished PNGs on the classic path.  They
                            // retain the icon pack's own alpha shape, while the
                            // main icons archive (rather than dynamicicons) makes
                            // this path win over the stock Adaptive resources.
                            val packageRoot = "animating_icons/$relativeName"
                            appendZipText(zip, "$packageRoot/fancy/manifest.xml", manifest)
                            renderedDays.forEachIndexed { index, bytes ->
                                appendZipBytes(
                                    zip,
                                    "$packageRoot/fancy/day_${index + 1}.png",
                                    bytes,
                                )
                            }
                            appendZipBytes(zip, "$packageRoot/quiet/quietImage.png", quiet)
                            convertedTrees++
                        }
                    }
            }
            if (convertedTrees == 0) {
                temporary.delete()
                destination.delete()
                return null
            }
            ZipFile(temporary).use { zip ->
                require(zip.entries().asSequence().any { it.name.endsWith("/fancy/manifest.xml") }) {
                    "HyperOS 动态日历归档校验失败"
                }
            }
            if (destination.exists() && !destination.delete()) {
                throw IllegalStateException("无法替换旧的动态日历归档")
            }
            if (!temporary.renameTo(destination)) {
                throw IllegalStateException("无法提交动态日历归档")
            }
            return destination
        } catch (throwable: Throwable) {
            temporary.delete()
            throw throwable
        }
    }

    /**
     * Mirrors IconCustomizer.getFileName()/getIconNames() so dynamic trees are
     * discoverable for both package-level and activity-level lookups.
     */
    private fun dynamicIconLookupName(packageName: String, className: String?): String {
        val resolvedClass = className?.takeIf { it.isNotBlank() } ?: return packageName
        return if (resolvedClass.startsWith(packageName)) {
            resolvedClass
        } else {
            "$packageName#$resolvedClass"
        }
    }

    private fun appendZipText(zip: ZipOutputStream, name: String, value: String) =
        appendZipBytes(zip, name, value.toByteArray(Charsets.UTF_8))

    private fun appendZipBytes(zip: ZipOutputStream, name: String, value: ByteArray) {
        zip.putNextEntry(ZipEntry(name))
        try {
            zip.write(value)
        } finally {
            zip.closeEntry()
        }
    }

