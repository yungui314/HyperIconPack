/**
 * Converts the public, de-facto standard appfilter.xml contract into the
 * exact icon archive layout consumed by HyperOS ThemeResourcesSystem.
 *
 * HyperOS opens /data/system/theme/icons as a normal ZIP and asks it for
 * entries such as:
 *
 *   res/drawable-xxhdpi/com.example.MainActivity.png
 *   res/drawable-xxhdpi/com.example#ExternalActivity.png
 *
 * The archive is intentionally generated before any Root operation.  This
 * keeps conversion inspectable and makes installation a separate, reversible
 * step.
 */
internal object HyperOsIconArchiveConverter {
    const val ORIGINAL_ICON_PACKAGE = "io.github.cl0ura.hypericonpack.source.original"
    const val ORIGINAL_ICON_LABEL = "本机原始图标"

    /** Kept only to recognise archives generated by pre-cache module builds. */
    const val ARCHIVE_FILE_NAME = "hypericonpack-icons.zip"
    private const val ARCHIVE_DIRECTORY = "hyperos-theme"
    private const val ARCHIVE_CACHE_DIRECTORY = "archives"
    private const val ARCHIVE_FILE_PREFIX = "icons-"
    private const val DYNAMIC_ARCHIVE_SUFFIX = "-dynamicicons.zip"
    /**
     * Version 17 retains the icon pack's complete appfilter contract, then
     * adds precise resources for desktop-launchable components and every
     * installed package.  This deliberately avoids baking thousands of
     * internal Activities into one archive: HyperOS can stop resolving icons
     * reliably when its private theme ZIP grows excessively large.  For
     * Global Monet, an application's API 33+ native monochrome layer is used
     * only when the selected icon pack has no usable mapping for that app.
     */
    private const val CURRENT_FORMAT_VERSION = 29
    private const val TARGET_DENSITY_DIRECTORY = "res/drawable-xxhdpi"
    private const val METADATA_ENTRY = "META-INF/hypericonpack-conversion.properties"
    private const val TRANSFORM_CONFIG_ENTRY = "transform_config.xml"
    private const val DEFAULT_RENDERING = "original"
    private const val GLOBAL_MONET_RENDERING = "global_monet_v18"
    private const val GLOBAL_MONET_RENDERING_PREFIX = "global_monet_v"
    private const val ALL_APPLICATIONS_SCOPE = "all"
    private const val SCOPE_FINGERPRINT_LENGTH = 20
    // A xxhdpi launcher icon is normally displayed at roughly 144 px on the
    // target device.  256 px leaves comfortable downsampling headroom while
    // reducing Monet's per-pixel work and temporary bitmap allocation by more
    // than half compared with the old 384 px output.
    private const val RENDER_SIZE_PX = 256
    private const val PROGRESS_REPORT_INTERVAL = 12
    private const val DYNAMIC_ICON_SIZE_PX = 256

    enum class ConversionPhase {
        PARSING,
        EXPLICIT_MAPPINGS,
        FALLBACK_ACTIVITIES,
        VALIDATING,
        COMPLETED,
    }

    /** A thread-safe, value-only snapshot delivered by [convert]. */
    data class ConversionProgress(
        val phase: ConversionPhase,
        val completed: Int,
        val total: Int,
        val explicitCompleted: Int = 0,
        val explicitTotal: Int = 0,
        val fallbackCompleted: Int = 0,
        val fallbackTotal: Int = 0,
    ) {
        val fraction: Float
            get() = if (total <= 0) 0f else completed.toFloat() / total.toFloat()
    }

    data class Result(
        val archive: File,
        val requestedExplicitMappings: Int,
        val convertedExplicitMappings: Int,
        val fallbackCandidates: Int,
        val convertedFallbackMappings: Int,
        val convertedPackageDefaults: Int,
        val nativeMonochromeIcons: Int,
        val skippedMappings: Int,
        val archiveSha256: String,
        val dynamicArchive: File?,
    ) {
        val convertedMappings: Int
            get() = convertedExplicitMappings + convertedFallbackMappings
    }

    /** Lightweight local state used only to make the settings UI truthful. */
    data class ExistingArchiveInfo(
        val archive: File,
        val iconPackPackage: String?,
        val fallbackScaleMultiplier: Float?,
        val globalMonetIcons: Boolean,
        val monetCustomColors: Boolean,
        val monetBackgroundColor: Int,
        val monetForegroundColor: Int,
        val applicationScopeFingerprint: String,
        val formatVersion: Int?,
    ) {
        val isCurrentFormat: Boolean
            get() = formatVersion == CURRENT_FORMAT_VERSION
    }

    /**
     * Android's native themed-icon layer supplies a semantic foreground only.
     * Keep its original adaptive icon alongside it so Monet can retain the
     * source icon's visible silhouette instead of imposing a circular frame.
     */
    private data class NativeMonochromeSource(
        val glyph: Drawable,
        val silhouette: Drawable,
        // Semantic monochrome resources can be broken by an OEM. Keep a
        // drawable that can always be written as a recognisable PNG instead
        // of treating a solid Monet block as a successful fallback.
        val recoveryDrawable: Drawable,
    )

    fun isOriginalIconSource(packageName: String?): Boolean = packageName == ORIGINAL_ICON_PACKAGE

    fun sourceLabel(packageName: String?): String = when {
        packageName == null -> "未选择图标来源"
        isOriginalIconSource(packageName) -> ORIGINAL_ICON_LABEL
        else -> packageName
    }

    /** Resolves the user-facing icon-pack application name when installed. */
    fun sourceLabel(context: Context, packageName: String?): String {
        if (packageName == null || isOriginalIconSource(packageName)) return sourceLabel(packageName)
        return runCatching {
            val info = context.packageManager.getApplicationInfo(
                packageName,
                PackageManager.MATCH_DISABLED_COMPONENTS,
            )
            context.packageManager.getApplicationLabel(info).toString().ifBlank { packageName }
        }.getOrDefault(packageName)
    }

    /**
     * Produces a validated ZIP in the app's external-files directory.  Root
     * can read that directory on stock Android, while ordinary apps cannot
     * modify the result without the module app's storage access.
     */
    fun convert(
        context: Context,
        iconPackPackage: String,
        fallbackScaleMultiplier: Float,
        globalMonetIcons: Boolean = false,
        monetCustomColors: Boolean = false,
        monetBackgroundColor: Int = IconPackConfig.DEFAULT_MONET_BACKGROUND_COLOR,
        monetForegroundColor: Int = IconPackConfig.DEFAULT_MONET_FOREGROUND_COLOR,
        includeInstalledAppFallbacks: Boolean,
        onProgress: ((ConversionProgress) -> Unit)? = null,
    ): Result {
        require(iconPackPackage.isNotBlank()) { "请先选择一个图标来源" }

        onProgress?.invoke(
            ConversionProgress(
                phase = ConversionPhase.PARSING,
                completed = 0,
                total = 0,
            ),
        )
        var parseFailure: Throwable? = null
        val pack = if (isOriginalIconSource(iconPackPackage)) {
            null
        } else {
            ParsedIconPack.load(context, iconPackPackage) { parseFailure = it }
                ?: throw IllegalStateException(
                    "无法读取 $iconPackPackage 的 appfilter.xml",
                    parseFailure,
                )
        }
        val allMappings = pack?.explicitMappings().orEmpty()
        if (pack != null) {
            require(allMappings.isNotEmpty()) { "图标包不含可转换的 ComponentInfo 图标映射" }
        }
        val scopeFingerprint = applicationScopeFingerprint(context)
        // Convert the icon pack's full public contract first. HyperOS vendor
        // aliases and non-launcher surfaces can request component names that
        // are not visible through ACTION_MAIN/CATEGORY_LAUNCHER, so filtering
        // appfilter by currently launchable activities makes coverage worse.
        val installedApplications = if (includeInstalledAppFallbacks) {
            installedApplications(context)
        } else {
            emptyList()
        }
        val installedPackageNames = installedApplications.mapTo(HashSet()) { it.packageName }
        // HyperOS uses the generated PNG as-is. Keep an OEM adaptive mask so
        // legacy original icons retain the same rounded system footprint they
        // would receive from the regular launcher pipeline.
        val systemAdaptiveMask = resolveSystemAdaptiveMask(context, installedApplications)
        val fallbackActivities = if (includeInstalledAppFallbacks) {
            // The complete appfilter mapping is already written above. For
            // installed applications not represented by it, add only actual
            // desktop launch targets; emitting every internal Activity bloats
            // an archive from ~8k to ~29k entries on this device and makes
            // HyperOS skip theme lookups under memory/ZIP pressure.
            launchableActivities(context).filter { info -> info.packageName in installedPackageNames }
        } else {
            emptyList()
        }
        val mappings = if (pack == null) {
            emptyList()
        } else {
            allMappings
        }
        val fallbackWorkTotal = fallbackActivities.size + installedApplications.size
        val progressTotal = mappings.size + fallbackWorkTotal
        onProgress?.invoke(
            ConversionProgress(
                phase = ConversionPhase.EXPLICIT_MAPPINGS,
                completed = 0,
                total = progressTotal,
                explicitTotal = mappings.size,
                fallbackTotal = fallbackWorkTotal,
            ),
        )

        // Every package/scale pair gets its own durable archive. Changing to a
        // previously converted pack is therefore only an install operation;
        // no full all-app render is repeated unless the user explicitly asks
        // to refresh it.
        val archive = archiveFile(
            context = context,
            iconPackPackage = iconPackPackage,
            fallbackScaleMultiplier = fallbackScaleMultiplier,
            globalMonetIcons = globalMonetIcons,
            monetCustomColors = monetCustomColors,
            monetBackgroundColor = monetBackgroundColor,
            monetForegroundColor = monetForegroundColor,
            applicationScopeFingerprint = scopeFingerprint,
            createDirectory = true,
        )
        val temporaryArchive = File(archive.parentFile, "${archive.name}.new")
        // FileOutputStream truncates a previous interrupted temporary result,
        // so a stale partial archive is never installed as a final artifact.

        var explicitConverted = 0
        val fallbackCandidates = installedApplications.size
        var fallbackConverted = 0
        var packageDefaultsConverted = 0
        var skipped = 0
        var monetCompatibilityRenders = 0
        var monetEmergencyFallbacks = 0
        var nativeMonochromeIcons = 0
        val entryNames = HashSet<String>()
        val successfullyConvertedComponents = HashSet<ComponentName>()
        // Every installed package receives one visual fallback.  Activity
        // aliases must all get their own ZIP entry for HyperOS lookup, but
        // rendering a separate identical Monet bitmap for each alias made a
        // full conversion needlessly take many minutes.  This cache holds at
        // most one compressed 256px PNG per installed package.
        val packageFallbackPngs = HashMap<String, ByteArray?>()
        // The same application icon can appear under multiple appfilter
        // components. Cache the resolved monochrome Drawable so the
        // conversion does not repeat PackageManager resource lookups for
        // every alias in a large icon pack. A null value is cached as well:
        // it means this app simply has no native themed-icon layer.
        val nativeMonochromeByPackage = HashMap<String, NativeMonochromeSource?>()
        val nativeMonochromeByComponent = HashMap<ComponentName, NativeMonochromeSource?>()
        val installedApplicationsByPackage = installedApplications.associateBy(ApplicationInfo::packageName)
        val launchableActivitiesByComponent = fallbackActivities
            .mapNotNull { info -> componentName(info)?.let { component -> component to info } }
            .toMap()
        val globalMonetPalette = if (globalMonetIcons) {
            SystemMonetIconPalette.global(
                context = context,
                useCustomColors = monetCustomColors,
                backgroundColor = monetBackgroundColor,
                foregroundColor = monetForegroundColor,
            )
        } else {
            null
        }
        try {
            ZipOutputStream(BufferedOutputStream(FileOutputStream(temporaryArchive))).use { zip ->
                // PNG already performs lossless DEFLATE internally. Running
                // the ZIP's default maximum compressor over thousands of PNGs
                // barely changes archive size but consumes a large share of
                // conversion time.
                zip.setLevel(Deflater.NO_COMPRESSION)
                writeArchiveMetadata(
                    zip = zip,
                    iconPackPackage = iconPackPackage,
                    fallbackScaleMultiplier = fallbackScaleMultiplier,
                    globalMonetIcons = globalMonetIcons,
                    monetCustomColors = monetCustomColors,
                    monetBackgroundColor = monetBackgroundColor,
                    monetForegroundColor = monetForegroundColor,
                    applicationScopeFingerprint = scopeFingerprint,
                )
                writeTransformConfig(zip, useDynamicIcon = false)
                fun renderDrawable(
                    drawable: Drawable,
                    nativeMonochrome: NativeMonochromeSource? = null,
                ): ByteArray? {
                    return try {
                        // Render first. If a malformed third-party Drawable
                        // throws after ZipOutputStream.putNextEntry(), a zero
                        // byte ZIP member remains and later archive validation
                        // fails even though the rest of conversion succeeded.
                        // Buffering one 384 px PNG avoids that poisoned entry.
                        val png = ByteArrayOutputStream()
                        if (globalMonetPalette != null) {
                            // A bad third-party Drawable must never remove the
                            // associated component from a global Monet archive.
                            // If its pixels cannot be rendered, retain a
                            // shaped Monet resource rather than falling back
                            // to HyperOS's original coloured app icon.
                            val monetRendered = runCatching {
                                if (nativeMonochrome != null) {
                                    GlobalMonetIconRenderer.renderNativeMonochrome(
                                        glyph = nativeMonochrome.glyph,
                                        silhouette = nativeMonochrome.silhouette,
                                        palette = globalMonetPalette,
                                        size = RENDER_SIZE_PX,
                                        output = png,
                                    )
                                } else {
                                    GlobalMonetIconRenderer.render(
                                        drawable = drawable,
                                        palette = globalMonetPalette,
                                        size = RENDER_SIZE_PX,
                                        output = png,
                                    )
                                }
                            }.isSuccess
                            if (!monetRendered) {
                                png.reset()
                                val compatibilityRendered = runCatching {
                                    GlobalMonetIconRenderer.renderSimplified(
                                        // A rejected native monochrome layer
                                        // must fall back to the complete source
                                        // artwork. Recolouring the silhouette
                                        // alone produces a blank colour tile.
                                        drawable = nativeMonochrome?.recoveryDrawable ?: drawable,
                                        palette = globalMonetPalette,
                                        size = RENDER_SIZE_PX,
                                        output = png,
                                    )
                                }.isSuccess
                                if (compatibilityRendered) {
                                    monetCompatibilityRenders++
                                } else {
                                    // A high-fidelity renderer can reject an
                                    // OEM Drawable for reasons unrelated to
                                    // its pixels. Before conceding coloured
                                    // output, use the deterministic two-tone
                                    // renderer: if the source can be drawn it
                                    // will remain a real Monet icon, never an
                                    // empty colour block or a large patch of
                                    // original-colour apps.
                                    png.reset()
                                    val guaranteedRendered = runCatching {
                                        GlobalMonetIconRenderer.renderGuaranteed(
                                            drawable = nativeMonochrome?.recoveryDrawable ?: drawable,
                                            palette = globalMonetPalette,
                                            size = RENDER_SIZE_PX,
                                            output = png,
                                        )
                                    }.isSuccess
                                    if (!guaranteedRendered) {
                                        monetEmergencyFallbacks++
                                        return null
                                    }
                                }
                            }
                        } else {
                            renderDrawableAsPng(drawable, png)
                        }
                        png.toByteArray()
                    } catch (_: Throwable) {
                        null
                    }
                }

                fun appendPng(entryName: String, png: ByteArray): Boolean {
                    return try {
                        zip.putNextEntry(ZipEntry(entryName))
                        zip.write(png)
                        zip.closeEntry()
                        true
                    } catch (_: Throwable) {
                        // Some commercial packs contain stale resources. No
                        // ZIP member was opened before rendering completed,
                        // so the validated archive can safely retain all
                        // other applications.
                        runCatching { zip.closeEntry() }
                        false
                    }
                }

                fun appendDrawable(
                    entryName: String,
                    drawable: Drawable,
                    nativeMonochrome: NativeMonochromeSource? = null,
                ): Boolean = renderDrawable(drawable, nativeMonochrome)
                    ?.let { png -> appendPng(entryName, png) }
                    ?: false

                fun nativeMonochromeForPackage(
                    packageName: String,
                    applicationInfo: ApplicationInfo?,
                ): NativeMonochromeSource? {
                    if (!globalMonetIcons || applicationInfo == null) return null
                    if (nativeMonochromeByPackage.containsKey(packageName)) {
                        return nativeMonochromeByPackage[packageName]
                    }
                    return nativeMonochromeSource(rawApplicationIcon(context, applicationInfo))
                        .also { nativeMonochromeByPackage[packageName] = it }
                }

                fun nativeMonochromeForComponent(component: ComponentName): NativeMonochromeSource? {
                    if (!globalMonetIcons) return null
                    if (nativeMonochromeByComponent.containsKey(component)) {
                        return nativeMonochromeByComponent[component]
                    }
                    val activityInfo = launchableActivitiesByComponent[component]
                    val applicationInfo = installedApplicationsByPackage[component.packageName]
                    // Activity icons may intentionally differ from the app's
                    // default icon. Prefer their native monochrome layer, but
                    // always fall back to the application's declared layer.
                    val layer = activityInfo?.let { info ->
                        nativeMonochromeSource(rawActivityIcon(context, info))
                    } ?: nativeMonochromeForPackage(component.packageName, applicationInfo)
                    nativeMonochromeByComponent[component] = layer
                    return layer
                }

                fun packageFallbackPng(applicationInfo: ApplicationInfo, packageName: String): ByteArray? {
                    if (packageFallbackPngs.containsKey(packageName)) {
                        return packageFallbackPngs[packageName]
                    }
                    // For an unmapped package, all Activity/alias entries use
                    // the package icon as their common visual fallback. This
                    // is both consistent on the desktop and lets one rendered
                    // PNG populate all component names for that package.
                    // A valid appfilter resource is always the user's chosen
                    // icon-pack artwork, so it wins even in Global Monet
                    // mode. Native monochrome is deliberately an unmapped-app
                    // enhancement rather than a replacement for an icon pack.
                    val iconPackDrawable = mappedPackageDrawable(
                        pack = pack,
                        packageName = packageName,
                        prepareForMonet = globalMonetIcons,
                    )
                    val rawIcon = rawApplicationIcon(context, applicationInfo)
                    // Build the icon-pack/System fallback first. When an app
                    // has Android's native monochrome glyph, that glyph is
                    // rendered *inside this prepared silhouette*. Previously
                    // we used the raw AdaptiveIconDrawable as the silhouette
                    // here, bypassing Pure's circular iconmask for every
                    // unmapped app and making a large part of the pack square
                    // as soon as Global Monet was enabled.
                    val preparedFallback = bestPackageDrawable(
                        pack = pack,
                        packageName = packageName,
                        rawIcon = rawIcon,
                        fallbackComponent = ComponentName(packageName, packageName),
                        fallbackScaleMultiplier = fallbackScaleMultiplier,
                        systemAdaptiveMask = systemAdaptiveMask,
                        prepareForMonet = globalMonetIcons,
                    )
                    val rawNativeMonochrome = if (iconPackDrawable == null) {
                        nativeMonochromeForPackage(packageName, applicationInfo)
                    } else {
                        null
                    }
                    val nativeMonochrome = rawNativeMonochrome?.let { native ->
                        preparedFallback?.let { silhouette ->
                            native.copy(silhouette = silhouette, recoveryDrawable = silhouette)
                        } ?: native
                    }
                    val drawable = iconPackDrawable
                        ?: nativeMonochrome?.glyph
                        ?: preparedFallback
                    if (nativeMonochrome != null) {
                        nativeMonochromeIcons++
                    }
                    val png = drawable?.let { renderDrawable(it, nativeMonochrome) }
                    packageFallbackPngs[packageName] = png
                    return png
                }

                if (pack != null) {
                    mappings.forEachIndexed { index, mapping ->
                        val entryName = archiveEntryName(mapping.component)
                        if (entryNames.contains(entryName)) {
                            // HyperOS resolves by Component name.  Duplicate XML
                            // items for the same component are ambiguous; retain
                            // the first one, matching the parser's map semantics.
                            skipped++
                        } else {
                            val iconPackDrawable = if (globalMonetIcons) {
                                pack.loadMappedDrawableForMonet(mapping.drawableName, mapping.component)
                            } else {
                                pack.loadMappedDrawable(mapping.drawableName)
                            }
                            // Some packs retain appfilter rows whose drawable
                            // was removed or placed in an unavailable split.
                            // Treat that as an unmapped installed app and use
                            // the pack's public iconback/iconmask fallback
                            // before considering the app's own monochrome
                            // layer. This keeps one broken row from becoming a
                            // square icon inside an otherwise circular pack.
                            val applicationInfo = installedApplicationsByPackage[mapping.component.packageName]
                            val activityInfo = launchableActivitiesByComponent[mapping.component]
                            val rawIcon = activityInfo?.let { rawActivityIcon(context, it) }
                                ?: applicationInfo?.let { rawApplicationIcon(context, it) }
                            val preparedFallback = if (iconPackDrawable == null && rawIcon != null) {
                                bestPackageDrawable(
                                    pack = pack,
                                    packageName = mapping.component.packageName,
                                    rawIcon = rawIcon,
                                    fallbackComponent = mapping.component,
                                    fallbackScaleMultiplier = fallbackScaleMultiplier,
                                    systemAdaptiveMask = systemAdaptiveMask,
                                    prepareForMonet = globalMonetIcons,
                                )
                            } else {
                                null
                            }
                            val rawNativeMonochrome = if (iconPackDrawable == null) {
                                nativeMonochromeForComponent(mapping.component)
                            } else {
                                null
                            }
                            val nativeMonochrome = rawNativeMonochrome?.let { native ->
                                preparedFallback?.let { silhouette ->
                                    native.copy(silhouette = silhouette, recoveryDrawable = silhouette)
                                } ?: native
                            }
                            val drawable = iconPackDrawable ?: nativeMonochrome?.glyph ?: preparedFallback
                            if (drawable == null) {
                                skipped++
                            } else if (appendDrawable(entryName, drawable, nativeMonochrome)) {
                                entryNames.add(entryName)
                                successfullyConvertedComponents.add(mapping.component)
                                explicitConverted++
                                if (nativeMonochrome != null) nativeMonochromeIcons++
                            } else {
                                skipped++
                            }
                        }
                        reportProgress(
                            callback = onProgress,
                            phase = ConversionPhase.EXPLICIT_MAPPINGS,
                            phaseCompleted = index + 1,
                            phaseTotal = mappings.size,
                            totalCompleted = index + 1,
                            total = progressTotal,
                            explicitCompleted = index + 1,
                            explicitTotal = mappings.size,
                            fallbackCompleted = 0,
                            fallbackTotal = fallbackWorkTotal,
                            force = index + 1 == mappings.size,
                        )
                    }
                }

                // Write exact resources for desktop launch targets. Package
                // resources below cover Settings, SystemUI and non-launcher
                // system surfaces without turning the archive into a copy of
                // every app's internal navigation Activity.
                fallbackActivities.forEachIndexed { index, activityInfo ->
                    val component = componentName(activityInfo)
                    if (component != null && component !in successfullyConvertedComponents) {
                        val entryName = archiveEntryName(component)
                        if (!entryNames.contains(entryName)) {
                            val png = packageFallbackPng(
                                applicationInfo = activityInfo.applicationInfo,
                                packageName = component.packageName,
                            )
                            if (png == null) {
                                skipped++
                            } else if (appendPng(entryName, png)) {
                                entryNames.add(entryName)
                                fallbackConverted++
                            } else {
                                skipped++
                            }
                        }
                    }
                    reportProgress(
                        callback = onProgress,
                        phase = ConversionPhase.FALLBACK_ACTIVITIES,
                        phaseCompleted = index + 1,
                        phaseTotal = fallbackWorkTotal,
                        totalCompleted = mappings.size + index + 1,
                        total = progressTotal,
                        explicitCompleted = mappings.size,
                        explicitTotal = mappings.size,
                        fallbackCompleted = index + 1,
                        fallbackTotal = fallbackWorkTotal,
                        force = index + 1 == fallbackWorkTotal,
                    )
                }

                // All installed packages includes system applications without
                // a MAIN/LAUNCHER activity.  Their package-level icon is what
                // Settings and many HyperOS system surfaces resolve.
                installedApplications.forEachIndexed { index, applicationInfo ->
                    val packageName = applicationInfo.packageName
                    val entryName = packageArchiveEntryName(packageName)
                    if (!entryNames.contains(entryName)) {
                        val png = packageFallbackPng(applicationInfo, packageName)
                        if (png == null) {
                            skipped++
                        } else if (appendPng(entryName, png)) {
                            entryNames.add(entryName)
                            packageDefaultsConverted++
                        } else {
                            skipped++
                        }
                    }
                    val completed = fallbackActivities.size + index + 1
                    reportProgress(
                        callback = onProgress,
                        phase = ConversionPhase.FALLBACK_ACTIVITIES,
                        phaseCompleted = completed,
                        phaseTotal = fallbackWorkTotal,
                        totalCompleted = mappings.size + completed,
                        total = progressTotal,
                        explicitCompleted = mappings.size,
                        explicitTotal = mappings.size,
                        fallbackCompleted = completed,
                        fallbackTotal = fallbackWorkTotal,
                        force = index + 1 == installedApplications.size,
                    )
                }
            }
            val totalConverted = explicitConverted + fallbackConverted
            require(totalConverted + packageDefaultsConverted > 0) { "所选应用的图标资源均无法渲染为 PNG" }
            onProgress?.invoke(
                ConversionProgress(
                    phase = ConversionPhase.VALIDATING,
                    completed = progressTotal,
                    total = progressTotal,
                    explicitCompleted = mappings.size,
                    explicitTotal = mappings.size,
                    fallbackCompleted = fallbackWorkTotal,
                    fallbackTotal = fallbackWorkTotal,
                ),
            )
            validateArchive(temporaryArchive, totalConverted + packageDefaultsConverted)
            if (archive.exists() && !archive.delete()) {
                throw IllegalStateException("无法替换旧的待应用主题归档")
            }
            if (!temporaryArchive.renameTo(archive)) {
                throw IllegalStateException("无法完成主题归档写入")
            }
        } catch (throwable: Throwable) {
            // Do not touch a previously successful final archive if this
            // conversion fails.  The .new file is deliberately ignored by
            // the Root installer and may be overwritten on the next attempt.
            throw throwable
        }

        val generatedDynamicArchive = generateDynamicCalendarArchive(
            pack = pack,
            installedPackageNames = installedPackageNames,
            launchableActivities = fallbackActivities,
            iconArchive = archive,
            globalMonetPalette = globalMonetPalette,
            fallbackScaleMultiplier = fallbackScaleMultiplier,
        )
        // HyperOS checks layer_animating_icons before classic animating_icons
        // whenever UseDynamicIcon=true.  The stock theme already contains
        // adaptive calendar trees, which forces square backgrounds even when a
        // pack PNG is circular.  Embed only the classic MAML tree into the main
        // icons archive and keep dynamicicons disabled; this makes the normal
        // IconCustomizer lookup select our dated PNGs without an adaptive mask.
        if (generatedDynamicArchive?.isFile == true) {
            embedClassicDynamicArchive(archive, generatedDynamicArchive)
            generatedDynamicArchive.delete()
        }
        val result = Result(
            archive = archive,
            requestedExplicitMappings = mappings.size,
            convertedExplicitMappings = explicitConverted,
            fallbackCandidates = fallbackCandidates,
            convertedFallbackMappings = fallbackConverted,
            convertedPackageDefaults = packageDefaultsConverted,
            nativeMonochromeIcons = nativeMonochromeIcons,
            skippedMappings = skipped,
            archiveSha256 = sha256(archive),
            dynamicArchive = null,
        )
        if (monetCompatibilityRenders > 0 || monetEmergencyFallbacks > 0) {
            Log.w(
                "HyperIconPack",
                "Monet renderer compatibility path: simplified=$monetCompatibilityRenders, emergency=$monetEmergencyFallbacks",
            )
        }
        onProgress?.invoke(
            ConversionProgress(
                phase = ConversionPhase.COMPLETED,
                completed = progressTotal,
                total = progressTotal,
                explicitCompleted = mappings.size,
                explicitTotal = mappings.size,
                fallbackCompleted = fallbackWorkTotal,
                fallbackTotal = fallbackWorkTotal,
            ),
        )
        return result
    }

    /**
     * Looks up the durable cache for one exact pack/scale combination. A
     * matching legacy archive is recognised as a one-time migration path.
     */
    fun existingArchive(
        context: Context,
        iconPackPackage: String?,
        fallbackScaleMultiplier: Float,
        globalMonetIcons: Boolean = false,
        monetCustomColors: Boolean = false,
        monetBackgroundColor: Int = IconPackConfig.DEFAULT_MONET_BACKGROUND_COLOR,
        monetForegroundColor: Int = IconPackConfig.DEFAULT_MONET_FOREGROUND_COLOR,
    ): File? {
        val packageName = iconPackPackage?.takeIf(String::isNotBlank) ?: return null
        val scopeFingerprint = applicationScopeFingerprint(context)
        val cached = archiveFile(
            context = context,
            iconPackPackage = packageName,
            fallbackScaleMultiplier = fallbackScaleMultiplier,
            globalMonetIcons = globalMonetIcons,
            monetCustomColors = monetCustomColors,
            monetBackgroundColor = monetBackgroundColor,
            monetForegroundColor = monetForegroundColor,
            applicationScopeFingerprint = scopeFingerprint,
            createDirectory = false,
        )
            .takeIf(File::isFile)
        if (cached != null && readArchiveInfo(cached)?.isCurrentFormat == true) return cached

        val legacy = legacyArchive(context).takeIf(File::isFile) ?: return null
        val info = readArchiveInfo(legacy)
        return legacy.takeIf {
                info?.isCurrentFormat == true &&
                info.iconPackPackage == packageName &&
                info.fallbackScaleMultiplier?.let { scale -> kotlin.math.abs(scale - fallbackScaleMultiplier) < 0.001f } == true &&
                info.globalMonetIcons == globalMonetIcons &&
                info.monetCustomColors == (globalMonetIcons && monetCustomColors) &&
                (!globalMonetIcons || !monetCustomColors ||
                    (info.monetBackgroundColor == monetBackgroundColor &&
                        info.monetForegroundColor == monetForegroundColor)) &&
                info.applicationScopeFingerprint == scopeFingerprint
        }
    }

