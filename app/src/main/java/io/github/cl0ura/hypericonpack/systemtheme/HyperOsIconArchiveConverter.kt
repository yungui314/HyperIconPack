    private fun launchableActivities(context: Context): List<ActivityInfo> {
        val launcherIntent = Intent(Intent.ACTION_MAIN).addCategory(Intent.CATEGORY_LAUNCHER)
        @Suppress("DEPRECATION")
        return context.packageManager.queryIntentActivities(launcherIntent, 0)
            .mapNotNull { it.activityInfo }
            .distinctBy { info -> "${info.packageName}/${info.name}" }
            .sortedWith(compareBy(ActivityInfo::packageName, ActivityInfo::name))
    }

    @Suppress("DEPRECATION")
    private fun installedApplications(context: Context): List<ApplicationInfo> =
        context.packageManager.getInstalledApplications(PackageManager.MATCH_DISABLED_COMPONENTS)
            .asSequence()
            .filter { info -> info.packageName.isNotBlank() }
            .distinctBy(ApplicationInfo::packageName)
